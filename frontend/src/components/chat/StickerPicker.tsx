export function StickerPicker({ isOpen, onClose, onSelectSticker }: StickerPickerProps) {
    const [selectedPack, setSelectedPack] = useState(STICKER_PACKS[0].id)
    const [searchQuery, setSearchQuery] = useState('')

    if (!isOpen) return null

    const currentPack = STICKER_PACKS.find(p => p.id === selectedPack) || STICKER_PACKS[0]

    const filteredStickers = searchQuery
        ? STICKER_PACKS.flatMap(pack => pack.stickers).filter(s => s.includes(searchQuery))
        : currentPack.stickers

    return (
        <div className="absolute bottom-full left-0 mb-2 w-80 bg-slate-800 rounded-xl border border-slate-700 shadow-xl overflow-hidden z-50">
            {/* Header */}
            <div className="flex items-center justify-between p-3 border-b border-slate-700">
                <h3 className="font-semibold text-white">Stickers</h3>
                <button
                    onClick={onClose}
                    className="text-slate-400 hover:text-white transition-colors"
                >
                    <X className="w-5 h-5" />
                </button>
            </div>

            {/* Search */}
            <div className="p-2 border-b border-slate-700">
                <div className="relative">
                    <Search className="absolute left-3 top-1/2 -translate-y-1/2 w-4 h-4 text-slate-400" />
                    <input
                        type="text"
                        value={searchQuery}
                        onChange={(e) => setSearchQuery(e.target.value)}
                        placeholder="Search stickers..."
                        className="w-full pl-9 pr-3 py-2 bg-slate-900 text-white text-sm rounded-lg border border-slate-700 focus:border-purple-500 focus:outline-none"
                    />
                </div>
            </div>

            {/* Pack Tabs */}
            {!searchQuery && (
                <div className="flex gap-1 p-2 border-b border-slate-700 overflow-x-auto">
                    {STICKER_PACKS.map(pack => {
                        const Icon = pack.icon
                        return (
                            <button
                                key={pack.id}
                                onClick={() => setSelectedPack(pack.id)}
                                className={`flex items-center gap-1.5 px-3 py-1.5 rounded-lg text-sm whitespace-nowrap transition-colors ${
                                    selectedPack === pack.id
                                        ? 'bg-purple-500 text-white'
                                        : 'text-slate-400 hover:text-white hover:bg-slate-700'
                                }`}
                            >
                                <Icon className="w-4 h-4" />
                                <span>{pack.name}</span>
                            </button>
                        )
                    })}
                </div>
            )}

            {/* Sticker Grid */}
            <div className="p-2 h-64 overflow-y-auto">
                <div className="grid grid-cols-8 gap-1">
                    {filteredStickers.map((sticker, index) => (
                        <button
                            key={`${sticker}-${index}`}
                            onClick={() => {
                                onSelectSticker(sticker)
                                onClose()
                            }}
                            className="w-8 h-8 flex items-center justify-center text-2xl hover:bg-slate-700 rounded-lg transition-colors"
                            title={sticker}
                        >
                            {sticker}
                        </button>
                    ))}
                </div>
                {filteredStickers.length === 0 && (
                    <div className="text-center text-slate-500 py-8">
                        No stickers found
                    </div>
                )}
            </div>
        </div>
    )
}
